#include "push_swap.h"

void	ft_free_stack(t_stack **stack)
{
	t_stack	*tmp;
	
	if (!stack || !*stack)
		return ;
	while (*stack)
	{
		tmp = (*stack)->next;
		free(*stack);
		*stack = tmp;
	}
	*stack = NULL;
}

void	ft_free_split(char **str)
{
	int	i;

	i = 0;
	if (!str)
		return ;
	while (str[i])
		free(str[i++]);
	free(str);
}

void	ft_error(t_stack **stack, char **str)
{
	if (stack && *stack)
		ft_free_stack(stack);
	if (str)
		ft_free_split(str);
	write(2, "Error\n", 6);
	exit(1);
}
